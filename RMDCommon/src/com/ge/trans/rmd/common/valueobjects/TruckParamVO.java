package com.ge.trans.rmd.common.valueobjects;

public class TruckParamVO {
	private String paramName;
	private String paramUnit;
	private String oneDayValue;
	private String thirtyDayAvgValue;
	private String lastMonthValue;
	private String lastQtrValue;
	private String lifeTimeValue;
	
	
	public String getParamName() {
		return paramName;
	}
	public void setParamName(String paramName) {
		this.paramName = paramName;
	}
	public String getParamUnit() {
		return paramUnit;
	}
	public void setParamUnit(String paramUnit) {
		this.paramUnit = paramUnit;
	}
	public String getOneDayValue() {
		return oneDayValue;
	}
	public void setOneDayValue(String oneDayValue) {
		this.oneDayValue = oneDayValue;
	}
	public String getThirtyDayAvgValue() {
		return thirtyDayAvgValue;
	}
	public void setThirtyDayAvgValue(String thirtyDayAvgValue) {
		this.thirtyDayAvgValue = thirtyDayAvgValue;
	}
	public String getLastMonthValue() {
		return lastMonthValue;
	}
	public void setLastMonthValue(String lastMonthValue) {
		this.lastMonthValue = lastMonthValue;
	}
	public String getLastQtrValue() {
		return lastQtrValue;
	}
	public void setLastQtrValue(String lastQtrValue) {
		this.lastQtrValue = lastQtrValue;
	}
	public String getLifeTimeValue() {
		return lifeTimeValue;
	}
	public void setLifeTimeValue(String lifeTimeValue) {
		this.lifeTimeValue = lifeTimeValue;
	}

	
	
	
	
}
