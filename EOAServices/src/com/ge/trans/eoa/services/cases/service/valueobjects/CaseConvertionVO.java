package com.ge.trans.eoa.services.cases.service.valueobjects;

public class CaseConvertionVO {
	
	private String year;
	private String month;
	private String convCount;
	private String rxCount;
	private String rxTitle;
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getConvCount() {
		return convCount;
	}
	public void setConvCount(String convCount) {
		this.convCount = convCount;
	}
	public String getRxCount() {
		return rxCount;
	}
	public void setRxCount(String rxCount) {
		this.rxCount = rxCount;
	}
	public String getRxTitle() {
		return rxTitle;
	}
	public void setRxTitle(String rxTitle) {
		this.rxTitle = rxTitle;
	}


}
